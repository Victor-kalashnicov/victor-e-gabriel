let idade = parseFloat(prompt("Digite sua idade!"))

if(idade < 12){
    alert("Você é uma criança!")
} else if (idade < 18){
    alert("Você é adolescente!")
}else if (idade < 64){
    alert("Você é adulto!")
}else if (idade <= 99){
    alert("Você é idoso!")
}else if (idade <= 130){
    alert("Você é centenário!")
}